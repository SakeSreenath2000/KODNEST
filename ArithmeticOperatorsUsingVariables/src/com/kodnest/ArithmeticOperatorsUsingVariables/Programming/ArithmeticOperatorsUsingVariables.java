package com.kodnest.ArithmeticOperatorsUsingVariables.Programming;

public class ArithmeticOperatorsUsingVariables {
	public static void main(String[] args) {
		int X=20;
		int Y=10;
		System.out.println("Addition is "+(X+Y));
		System.out.println("Subtraction is "+(X-Y));
		System.out.println("Multiplication is "+(X*Y));
		System.out.println("Division Quotient is "+(X/Y));
		System.out.println("Division Remainder is "+(X%Y));
	}

}
