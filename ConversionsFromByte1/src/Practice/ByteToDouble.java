package Practice;

public class ByteToDouble {

	public static void main(String[] args) {
		
		        byte myByte = 120;
		        double myDouble = myByte; 
		        // Implicit casting from byte to double

		        System.out.println("Original byte: " + myByte);
		        System.out.println("Converted double: " + myDouble);


	}

}
