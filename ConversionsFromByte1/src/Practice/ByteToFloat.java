package Practice;

public class ByteToFloat {
	public static void main(String[] args) {
		
		        byte myByte = 120;
		        float myFloat = myByte; 
		        // Implicit casting from byte to float

		        System.out.println("Original byte: " + myByte);
		        System.out.println("Converted float: " + myFloat);


	}

}
