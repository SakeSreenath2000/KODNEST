package Practice;

public class IntToLong {
	public static void main(String[] args) {
		
		        int myInt = 1000;
		        long myLong = myInt; 
		        // Implicit conversion from int to long

		        System.out.println("Original int: " + myInt);
		        System.out.println("Converted long: " + myLong);


	}

}
