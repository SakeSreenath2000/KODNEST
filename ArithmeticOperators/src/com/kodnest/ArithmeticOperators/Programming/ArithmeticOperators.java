package com.kodnest.ArithmeticOperators.Programming;

public class ArithmeticOperators {
	public static void main(String[] args) {
		System.out.println("Addition is "+(10+20));
		System.out.println("Subtraction is "+(20-10));
		System.out.println("Multiplication is "+(10*2));
		System.out.println("Division Quotient is "+(10/2));
		System.out.println("Division Remainder is "+(10%2));
	}

}
