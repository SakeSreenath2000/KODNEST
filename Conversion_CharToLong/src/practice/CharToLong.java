package practice;

public class CharToLong {
	public static void main(String[] args) {
		        char myChar = 'D';
		        long myLong = myChar; 
		        // Implicit casting from char to long

		        System.out.println("Original char: " + myChar);
		        System.out.println("Converted long: " + myLong);

	}

}
