package Practice;

public class ByteToInt {
	public static void main(String[] args) {
		
		        byte myByte = 120;
		        int myInt = myByte; 
		        // Implicit conversion from byte to int

		        System.out.println("Original byte: " + myByte);
		        System.out.println("Converted int: " + myInt);

	}

}
