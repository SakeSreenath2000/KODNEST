package Practice;

public class ByteToShort {
	public static void main(String[] args) {
		
		        byte myByte = 100;
		        short myShort = myByte; 
		        // Implicit casting from byte to short

		        System.out.println("Original byte: " + myByte);
		        System.out.println("Converted short: " + myShort);


	}

}
