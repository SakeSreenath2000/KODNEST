package com.kodnest.PatternProgramming;

public class Pattern7 {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=5;j++)
			{
				System.out.print(j);//here we are printing the position(Column Number) of numbers from J=1 to J<=5 in Every Line
			}
			System.out.println();
		}
		
	}

}
