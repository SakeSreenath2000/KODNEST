package Practice;

public class ShortToLong {

	public static void main(String[] args) {
		
		        short myShort = 1000;
		        long myLong = myShort; 
		        // Implicit conversion from short to long

		        System.out.println("Original short: " + myShort);
		        System.out.println("Converted long: " + myLong);


	}

}
