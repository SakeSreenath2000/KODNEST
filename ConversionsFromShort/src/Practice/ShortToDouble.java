package Practice;

public class ShortToDouble {
	public static void main(String[] args) {

		        short myShort = 1000;
		        double myDouble = myShort; 
		        // Implicit casting from short to double

		        System.out.println("Original short: " + myShort);
		        System.out.println("Converted double: " + myDouble);


	}

}
