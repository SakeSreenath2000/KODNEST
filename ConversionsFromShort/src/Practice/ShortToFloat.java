package Practice;

public class ShortToFloat {

	public static void main(String[] args) {
		
		        short myShort = 1000;
		        float myFloat = myShort; 
		        // Implicit casting from short to float

		        System.out.println("Original short: " + myShort);
		        System.out.println("Converted float: " + myFloat);


	}

}
