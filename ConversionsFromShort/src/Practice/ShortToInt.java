package Practice;

public class ShortToInt {

	public static void main(String[] args) {
		
		        short myShort = 1000;
		        int myInt = myShort; 
		        // Implicit conversion from short to int

		        System.out.println("Original short: " + myShort);
		        System.out.println("Converted int: " + myInt);


	}

}
