package com.kodnest.LoopingControlConstructVariations.Programming;

public class LoopVariations_2 {
	public static void main(String[] args) {
		int s=0;
		int count=0;
		do
		{
			System.out.println("Kodnest");
			s++;count++;//we can also write like this after semicolon
		}while(s<=5);
		System.out.println(count);
	}
	

}
