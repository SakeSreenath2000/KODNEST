package com.kodnest.LoopingControlConstructVariations.Programming;

public class LoopVariations_1 {
	public static void main(String[] args) {
		int s=0;
		int count=0;
		while(s<=5)
		{
			System.out.println("Kodnest");
			s+=1;//i.e s=s+1
			count+=1;//i.e count=count+1
		}
		System.out.println(count);
	}
	

}
